/* C program for Merge Sort */
#include <stdio.h>
#include <stdlib.h>
#define M 60000
void merge(int arr[], int l, int m, int r)
{
    int i, j, k;
    int n1 = m - l + 1;
    int n2 = r - m;

    /* create temp arrays */
    int L[n1], R[n2];

    /* Copy data to temp arrays L[] and R[] */
    for (i = 0; i < n1; i++)
        L[i] = arr[l + i];
    for (j = 0; j < n2; j++)
        R[j] = arr[m + 1 + j];

    /* Merge the temp arrays back into arr[l..r]*/
    i = 0; // Initial index of first subarray
    j = 0; // Initial index of second subarray
    k = l; // Initial index of merged subarray
    while (i < n1 && j < n2)
    {
        if (L[i] <= R[j])
        {
            arr[k] = L[i];
            i++;
        }
        else
        {
            arr[k] = R[j];
            j++;
        }
        k++;
    }

    /* Copy the remaining elements of L[], if there
    are any */
    while (i < n1)
    {
        arr[k] = L[i];
        i++;
        k++;
    }

    /* Copy the remaining elements of R[], if there
    are any */
    while (j < n2)
    {
        arr[k] = R[j];
        j++;
        k++;
    }
}

void mergeSort(int arr[], int l, int r)
{
    if (l < r)
    {
        int m = l + (r - l) / 2;
        mergeSort(arr, l, m);
        mergeSort(arr, m + 1, r);
        merge(arr, l, m, r);
    }
}

/* UTILITY FUNCTIONS */
/* Function to print an array */
void printArray(int A[], int size)
{
    int i;
    for (i = 0; i < size; i++)
        printf("%d ", A[i]);
    printf("\n");
}
int main()
{
    FILE* FP=fopen("mer.txt","r");
    int arr[M];
    for(int i=0;i<M;i++)
    {
        fscanf(FP,"%d",&arr[i]);
    }
    printf("Given array is \n");
    mergeSort(arr, 0, M - 1);
    printf("\nSorted array is \n");
    printArray(arr, M);
    FILE*fp=fopen("CONCLUSION.txt","a");
    fprintf(fp,"\nThe time complexity of MERGE sort is O(n*log(n))\nIt is better than bubble and Quick sort in any cases\nBut its space complexity is O(n) as it uses a extra array\n");
    return 0;
}
